#include <unistd.h>

void	ft_putnbr(int nb)
{
	char a;

	a = '0';
	if (nb < 0)
	{
		write(1, "-", 1);
		ft_putnbr(-nb);
	}
	else if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
	{
		a = (nb + 48);
		write(1, &a, 1);
	}
}
