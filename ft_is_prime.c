int		ft_is_prime(int nb)
{
	int i;

	i = 1;
	if (nb < 0 || nb == 0 || nb == 1 || nb > 2147483647)
		return (0);
	while (nb - i > 1)
	{
		if (nb % (nb - i) == 0)
			return (0);
		else
			i++;
	}
	return (1);
}
